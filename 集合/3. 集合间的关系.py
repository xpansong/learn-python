print('----------判断两个集合是否相等------------')
s1={10,20,30,40}
s2={40,20,30,10}
print(s1==s2)  #虽然元素顺序不同，但内容是相同，集合是无序的
print(s1!=s2)

print('---------判断一个集合是否为另一个集合的子集--------------')
s1={10,20,30,40,50,60}
s2={10,20,30,40}
s3={10,20,90}
print(s2. issubset(s1))
print(s3.issubset(s1))

print('---------判断一个集合是否为另一个集合的超集--------------')
'''什么是超集？B是A的子集，那么A就是B的超集'''
print(s1.issuperset(s2))

print('----------判断两个集合是否有交集-------------')
print(s2.isdisjoint(s3))
s4={100,200,300}
print(s2.isdisjoint(s4))

